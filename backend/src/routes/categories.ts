import express from "express";
import {
  createCategories,
  deleteCategoryById,
  getAllCategories,
  updateCategoryById,
} from "../controller/categories";
import { PERMISSION } from "@prisma/client";
import { authenticate, authorize } from "../middleware/auth";
import { validateReqBody, validateReqId } from "../middleware/validator";
import { categoryByIdSchema, createCategorySchema } from "../schema/categories";
const router = express();
/**
 * @swagger
 * /categories:
 *   post:
 *     summary: Create a new category
 *     tags: [Categories]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/CreateCategoryInput'
 *     responses:
 *       201:
 *         description: Category created successfully
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Category'
 *       400:
 *         description: Invalid input
 *       401:
 *         description: Unauthorized
 *       403:
 *         description: Forbidden
 */
router.post(
  "/",
  validateReqBody(createCategorySchema),
  authenticate(),
  authorize([PERMISSION.SUPER_ADMIN_POST, PERMISSION.ADMIN_POST]),
  createCategories
);
/**
 * @swagger
 * /categories:
 *   get:
 *     summary: Get all categories
 *     tags: [Categories]
 *     responses:
 *       200:
 *         description: List of all categories
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 *               items:
 *                 $ref: '#/components/schemas/Category'
 */
router.get(
  "/",

  getAllCategories
);
/**
 * @swagger
 * /categories/{id}:
 *   delete:
 *     summary: Delete a category by ID
 *     tags: [Categories]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     responses:
 *       200:
 *         description: Category deleted successfully
 *       400:
 *         description: Invalid input
 *       401:
 *         description: Unauthorized
 *       403:
 *         description: Forbidden
 *       404:
 *         description: Category not found
 */
router.delete(
  "/:id",
  validateReqId(categoryByIdSchema),
  authenticate(),
  authorize([PERMISSION.SUPER_ADMIN_DELETE, PERMISSION.ADMIN_DELETE]),
  deleteCategoryById
);
/**
 * @swagger
 * /categories/{id}:
 *   put:
 *     summary: Update a category by ID
 *     tags: [Categories]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/UpdateCategoryInput'
 *     responses:
 *       200:
 *         description: Category updated successfully
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Category'
 *       400:
 *         description: Invalid input
 *       401:
 *         description: Unauthorized
 *       403:
 *         description: Forbidden
 *       404:
 *         description: Category not found
 */
router.put(
  "/:id",
  validateReqId(categoryByIdSchema),
  authenticate(),
  authorize([PERMISSION.SUPER_ADMIN_PUT, PERMISSION.ADMIN_PUT]),
  updateCategoryById
);
export default router;
